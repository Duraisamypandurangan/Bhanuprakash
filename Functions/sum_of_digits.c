/* -----------------------------------------------------------------------------

FUNCTION NAME:sum_of_digits

DESCRIPTION: This function is to find sum of digits using recursive function.

PARAMETER DESCRIPTION: int num

RETURN VALUE: sum of digits

----------------------------------------------------------------------------- */
int sum_of_digits(int num)
{
    if(num % 10 == 0)//base case
        return num;
    return num % 10 + sum_of_digits(num/10);//recursive case
}
/* -----------------------------------------------------------------------------*/

/********************************************************************************
 **--main()--
 *******************************************************************************/
int main()
{
    int num,sum;
    printf(" Enter the number :");
    scanf("%d",&num);//Read input
    sum=sum_of_digits(num);//Function call to find sum of digits
    printf(" The sum of digits is %d\n",sum);//Print out put
    return 0;
}                 
