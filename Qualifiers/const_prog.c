int main(void)
{
int num1=11,num2=12;
int const *ptr=&num1; //Pointer value is constant
int *const cptr=&num2;//Pointer Adress is constant
printf("PTR=%p CPTR=%p\n*PTR=%d *CPTR=%d\n",ptr,cptr,*ptr,*cptr);
(ptr)++;// we can change address contain in ptr

/* if we run this we ll get error refer line NO 58 */
//(cptr)++; here we cant change address hold in Cptr
//(*ptr)++; here we cant change value hold in Ptr

(*cptr)++; //we can change value hold in Cptr
printf("PTR=%p CPTR=%p\n*PTR=%d *CPTR=%d\n",ptr,cptr,*ptr,*cptr);
return 0;
}
