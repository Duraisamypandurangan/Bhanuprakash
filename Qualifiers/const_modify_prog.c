int main(void)
{
	const int num=10;//declaration of constant variable
	
	int *ptr= &num;//deaclare and assign num address to ptr variable
	
	printf("before modify the constant num= %d\n",num);

	/*	num++;   we can't modify using const variable directly. 
             we will get Error refer line No 54*/ 
	
	(*ptr)++;//we can modify indirectly using pointers
	
	printf("after modifying the constant num= %d\n",num);
	return 0;
}

