int main(void)
{
	signed int num,n;
	printf("Enter the number to find absulute number:");
	scanf("%d",&num);//read input
	n=num;//assign num to n to keep orginal 
	abs(n);//macro call to find absolute number
	printf("absulute number of %d is %d\n",num,n);//print final output
	return 0;
}
