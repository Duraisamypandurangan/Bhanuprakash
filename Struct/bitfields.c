struct date {
 
  /* In this require 3 interger data types to hold data total size is 12 bytes
    unsigned int d;
    unsigned int m;
    unsigned int y;
  */
  	
  	
  	/* here We required only 4 bytes enough to hold same data we can optimize 
  	 data using bit fields */
  	unsigned int d : 5;
    unsigned int m : 4;
    unsigned int y : 21;
};
 
int main()
{
    printf("Size of date is %lu bytes\n",sizeof(struct date));
    struct date dt = { 10, 10, 1997 };
    printf("Date is %u/%u/%u\n", dt.d, dt.m, dt.y);
    return 0;
}
