/*******************************************************************************
** -- Global Data Declarations --
*******************************************************************************/
 struct record
{
	char name[20];
	int rollno;
};
union student
{
	struct record data;
	int marks;
	char grade;
};
/*******************************************************************************
** -- Macros --
*******************************************************************************/

/*******************************************************************************
** -- Global Variables Initializations --
*******************************************************************************/

/*******************************************************************************
** -- Function Prototypes --
*******************************************************************************/

/*******************************************************************************
** -- Functions --
*******************************************************************************/


/*******************************************************************************
**--main()Function--
********************************************************************************/
int main(void)
{
	union student stu;
	strcpy(stu.data.name,"Krish");
	stu.data.rollno=108;
	stu.grade='A';
	printf("Name=%s\nRollNo=%d\nGrade=%c\n",stu.data.name,stu.data.rollno,stu.grade);
	return 0;
}
