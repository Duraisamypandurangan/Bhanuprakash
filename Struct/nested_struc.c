/*******************************************************************************
** -- Global Data Declarations --
*******************************************************************************/
 struct date
{
	int day;
	int month;
	int year;
};
struct student
{
	char name[20];
	int roll_no;
	struct date dob;
};
/*******************************************************************************
** -- Macros --
*******************************************************************************/

/*******************************************************************************
** -- Global Variables Initializations --
*******************************************************************************/

/*******************************************************************************
** -- Function Prototypes --
*******************************************************************************/

/*******************************************************************************
** -- Functions --
*******************************************************************************/


/*******************************************************************************
**--main()Function--
********************************************************************************/
int main(void)
{
	struct student stu;
	strcpy(stu.name,"Ram");
	stu.roll_no=100;
	stu.dob.day=10;
	stu.dob.month=10;
	stu.dob.year=1997;
	printf("The %s Roll_no is %d\n",stu.name,stu.roll_no);
	printf("the %s Born in %d %d %d\n",stu.name,stu.dob.day,stu.dob.month,stu.dob.year);
	return 0;
}
	
