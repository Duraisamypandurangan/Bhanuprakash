int main(void)
{
/*Declaration of int,char,float and double pointer*/
char *cptr=0;
int *iptr=0;
float *fptr=0;
double *dptr=0;
/*To get the size of each data type*/
cptr++;
iptr++;
fptr++;
dptr++;
/*print the size of each data type */
printf("size of char    %d bytes\n",cptr);
printf("size of int     %d bytes\n",iptr);
printf("size of float   %d bytes\n",fptr);
printf("size of double  %d bytes\n",dptr);
}
