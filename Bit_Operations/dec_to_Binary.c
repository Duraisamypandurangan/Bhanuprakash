/* -----------------------------------------------------------------------------

FUNCTION NAME:Void

DESCRIPTION: To print Binary Format of given Integer

PARAMETER DESCRIPTION: int num

RETURN VALUE: void

----------------------------------------------------------------------------- */
void Displaybits(int num)
{
  int mask,i;

  for(i=31;i>=0;i--)
  {
  	  mask=1<<i;
  	  putchar((mask & num)?'1':'0');
  	  putchar(' ');
  	  if(i%8==0)
  	  	  printf("  ");
  }
  printf("\n");
}


