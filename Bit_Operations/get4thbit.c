/*******************************************************************************
**--main()Function--
********************************************************************************/
int main(void)
{
	//Declare variables
	int num,mask,i;
	char op;
	//read input
	printf("Enter the number :");
	scanf("%d",&num);
	mask=1<<3;//create mask
        //to check 4th bit High or Low	
	if(num&mask)
		printf("The 4th Bit is high\n");
	else
		printf("The 4th Bit is Low\n");
	printf("Do you want to display %d in Binary Y or N :",num);
	scanf(" %c",&op);
	//To Display Bits In Binary
	if(op=='y' || op=='Y')
	{
		for(i=31;i>=0;i--)
		{
			mask=1<<i;
			putchar((mask & num) ? '1':'0');
			putchar(' ');
			if(i % 8==0)
				printf("   ");
		}
		printf("\n");
	}
return 0;
}
	
