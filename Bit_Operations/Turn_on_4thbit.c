/* -----------------------------------------------------------------------------

FUNCTION NAME:DisplayBits

DESCRIPTION: To Display Integer In Binary

PARAMETER DESCRIPTION: int num

RETURN VALUE: void

----------------------------------------------------------------------------- */
void DisplayBits(int num)
{
	int i,mask;
		for(i=31;i>=0;i--)
		{
			mask=1<<i;
			putchar((mask & num) ? '1':'0');
			putchar(' ');
			if(i % 8==0)
				printf("   ");
		}
		printf("\n");
}

/* ----------------------------------------------------------------------------*/

/*******************************************************************************
**--main()Function--
********************************************************************************/


int main(void)
{
	//Declare variables	
	int num,mask;
	//Read Input
	printf("Enter the number :");
	scanf("%d",&num);
	printf("%d in Binary\n",num);
	mask=(1<<3);//create mask
	if(mask&num)//To Check bit is High or Low
	{
		printf("Inintially 4th Bit is ON\n");
		DisplayBits(num);
	}
	else
	{
		DisplayBits(num);
		num|=mask;//Make the 4th Bit High
		printf("After 4th Bit is ON\n");
		printf("The num value became %d, The Binary pattren is :\n",num);
		DisplayBits(num);//Function Call to Display Integer In Binary
	}
	return 0;
}

