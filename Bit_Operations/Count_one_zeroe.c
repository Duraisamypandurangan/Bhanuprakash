/* -----------------------------------------------------------------------------

FUNCTION NAME:DisplayBits

DESCRIPTION: To Display Integer In Binary

PARAMETER DESCRIPTION: int num

RETURN VALUE: void

----------------------------------------------------------------------------- */
void DisplayBits(int num)
{
	int i,mask;
		for(i=31;i>=0;i--)
		{
			mask=1<<i;
			putchar((mask & num) ? '1':'0');
			putchar(' ');
			if(i % 8==0)
				printf("   ");
		}
		printf("\n");
}

/* ----------------------------------------------------------------------------*/

/* -----------------------------------------------------------------------------

FUNCTION NAME:Count_Ones_Zeros

DESCRIPTION: To counts ones and zero in Binary format ofInteger

PARAMETER DESCRIPTION: int num

RETURN VALUE: void


----------------------------------------------------------------------------- */
void Count_Ones_Zeros(int num)
{
	int mask,i,One=0,Zero=0;
	for(i=31;i>=0;i--)
	{
		mask=1<<i;
		if(num&mask)
			One++;
		else
			Zero++;
	}
	printf("%d One's & %d Zero's\n",One,Zero);
}

