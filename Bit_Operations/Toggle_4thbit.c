/* -----------------------------------------------------------------------------

FUNCTION NAME:DisplayBits

DESCRIPTION: To Display Integer In Binary

PARAMETER DESCRIPTION: int num

RETURN VALUE: void

----------------------------------------------------------------------------- */
void DisplayBits(int num)
{
	int i,mask;
		for(i=31;i>=0;i--)
		{
			mask=1<<i;
			putchar((mask & num) ? '1':'0');
			putchar(' ');
			if(i % 8==0)
				printf("   ");
		}
		printf("\n");
}

/* ----------------------------------------------------------------------------*/

/*******************************************************************************
**--main()Function--
********************************************************************************/
int main(void)
{
	int num,mask;//Declare Variable
	//Read Input
	printf("Enter the number :");
	scanf("%d",&num);
	printf("%d in Binary\n",num);
	DisplayBits(num);//Function Call to Display Integer in Binary 
	mask=(1<<3);//create mask
	num^=mask;//Toggle 4th Bit
	printf("After Toggle num Value Became %d, The Binary pattern: \n",num);
	DisplayBits(num);
	return 0;
}



