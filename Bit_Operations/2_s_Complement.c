void displayBits(int x)
{
    int i,mask;
    for(i=31; i>=0; i--)
    {   
        mask = 1<<i;
        putchar((x & mask)?'1':'0'); /* Test and print ith bit*/
        if(i%8==0)
            putchar(' '); /*Space after 8 bits*/
    }
    printf("\n");
}

/* ----------------------------------------------------------------------------*/

/*******************************************************************************
**--main()Function--
********************************************************************************/
int main(void)
{
    int num,i,mask;
    printf("Enter a number : ");
    scanf("%d",&num);
    displayBits(num);
    printf("Two's complement is : \n");
    displayBits(~num+1);
    return 0;
}

