 int Num;
/*******************************************************************************
** -- Macros --
*******************************************************************************/

/*******************************************************************************
** -- Global Variables Initializations --
*******************************************************************************/

/*******************************************************************************
** -- Function Prototypes --
*******************************************************************************/
void Update_Num(void);
extern void print(void);
/*******************************************************************************
** -- Functions --
*******************************************************************************/

/* -----------------------------------------------------------------------------

FUNCTION NAME:Update_Num

DESCRIPTION: This function is to update the number

PARAMETER DESCRIPTION: void

RETURN VALUE: void

----------------------------------------------------------------------------- */
void Update_Num()
{
	Num++; //Increment the global variable.
	print();//fuction call to print updated number.
	Num--;//decrement the global variable.
	print();
	Num=Num+2;//add 2 to the number.
	print();
}

/* -----------------------------------------------------------------------------*/
/********************************************************************************
 **--main()Function--
 ********************************************************************************/
int main(void)
{
	Update_Num();//Function call to update Number.
	return 0;
}
