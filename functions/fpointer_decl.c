/* -----------------------------------------------------------------------------

FUNCTION NAME:Add_2num

DESCRIPTION: To add and print two numbers

PARAMETER DESCRIPTION: int Num1 int Num2

RETURN VALUE: void

----------------------------------------------------------------------------- */
void Add_2num(int Num1,int Num2)
{
	printf("The sum of %d + %d is %d\n",Num1,Num2,(Num1+Num2));//Add and print two numbers
}
