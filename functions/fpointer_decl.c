{
	int Num1,Num2;
	printf("Enter the two numbers :");
	scanf("%d%d",&Num1,&Num2); //Read two numbers
	void (*ptr)(int, int);//declare function pointer
    ptr=Add_2num;//assign function address to pointer
    ptr(Num1,Num2); //function call using pointer
    return 0;
}

